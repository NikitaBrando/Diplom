<br>
Помощь государства позволила обеспечить сотрудников парка современной техникой. Хотя, как отметил директор, все необходимое пока не удалось закупить. Всего на территории ― 16 кордонов, а оснастить хотя бы одним снегоходом или моторной лодкой удалось лишь половину из них.</p>
