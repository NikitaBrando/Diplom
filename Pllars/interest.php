  	<h3>Новая техника в национальном парке «Ленские столбы»</h3>
  	<p>Природный парк «Ленские столбы» получил целевую субсидию от Министерства природных ресурсов России. Благодаря этому инспекторы смогли приобрести технику и специальные средства. Их использование повысило эффективность охраны уникальной территории. Также планируется принять меры по привлечению туристов.<br> <br>
