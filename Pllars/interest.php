  	<h3>Наука</h3>
  	<p>Научно-исследовательская деятельность Природного парка направлена на разработку и внедрение научных методов сохранения биологического разнообразия природных и историко-культурных комплексов и объектов в условиях рекреационного и хозяйственного использования, долговременное слежение за динамикой природных процессов, оценку и прогноз экологической обстановки в регионе, воспроизводства и рационального использования природных ресурсов...
<br>
Научно-исследовательская деятельность Природного парка направлена на разработку и внедрение научных методов сохранения биологического разнообразия природных и историко-культурных комплексов и объектов в условиях рекреационного и хозяйственного использования, долговременное слежение за динамикой природных процессов, оценку и прогноз экологической обстановки в регионе, воспроизводства и рационального использования природных ресурсов.
<br>
Научно-исследовательская деятельность в Природном парке проводится:
<br>
- Научно-исследовательскими учреждениями и высшими учебными заведениями соответствующего профиля на договорных началах по общим с Природным парком программам, согласованным с Министерством охраны природы Республики Саха (Якутия).
<br>
Для проведения научно-исследовательских работ природоохранного направления в границах Природного парка может быть создана биологическая станция или иное научное подразделение, направление деятельности которого должно быть согласовано с руководством Природного парка.
<br>
Планы биотехнических, рекультивационных, реставрационных и лесохозяйственных работ, а также мероприятий по регулированию численности диких животных на территории Природного парка рассматриваются в обязательном порядке на заседаниях научно-технического совета.
<br>
Большая работа по описанию и оценке значимости объектов историко-культурного и природного наследия на территории парка была проведена на стадии проектных работ при создании парка в 1992-1994 г.г.
<br>
Археологические изыскания были проведены археологической экспедицией Якутского государственного университета. Археологические раскопки Диринг-Юряхской палеолитической стоянки древнего человека были произведены Приленской экспедицией института ЯФ СО РАН под руководством академика Ю.А. Мочанова.
<br>
Со времени создания парка научно-исследовательские работы на территории парка проведены как отечественными, так и зарубежными учеными, студентами и участниками природоохранных организаций.
<br>
Ежегодно на территории парка работают группы сотрудников института биологических проблем криолитозоны (ИБПК) СО РАН, преподавателей и студентов Якутского государственного университета имени М.К. Амосова. Трижды приезжал с группой студентов из Гроссвальдского университета и общества охраны природы Германии (NABU) альтернативный нобелевский лауреат, профессор М. Зукков (1997,1998, 2003).
<br>
Ранние палеонтологические исследования проводились еще в XIX веке Н.Г. Меглицким (1850 г.), А.Л. Чекановским (1875 г.), Е.В. Лермонтовой и другими.
<br>
Из якутских ученых геологическими и палеонтологическими исследованиями занимается профессор П.И. Колосов.
В 2006-2007 г.г. на территории парка работала экспедиция палеонтологического института РАН под руководством члена-корреспондента РАН, академика Ю.А. Розанова.</p>
